public class LogicalNOToperator {
      public static void main(String[] args) {
            //initializing the values.
            int a =  10, b = 1;

            System.out.println("var1: "+a);
            System.out.println("var2: "+b);

            System.out.println("!(a < b): "+!(a < b));
            System.out.println("!(a > b): "+!(a > b));
      }
}